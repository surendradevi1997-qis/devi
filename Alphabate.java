import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class Alphabate
{
	public static void main (String[] args) throws java.lang.Exception
	{
		char ch='a';
		if(ch=='a')
		{
			System.out.println(ch+ "is an alphabate");
		}
		else
		{
			System.out.println(ch+ "is a number");
		}
	}
}
